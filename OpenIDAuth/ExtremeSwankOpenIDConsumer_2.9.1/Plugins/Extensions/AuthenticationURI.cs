using System;
using System.Collections.Generic;
using System.Text;

namespace ExtremeSwank.Authentication.OpenID.Plugins.Extensions
{
    /// <summary>
    /// Some static methods that return authentication policy URIs.
    /// </summary>
    public static class AuthenticationURI
    {
        /// <summary>
        /// Returns the URI for Phishing-Resistant Authentication
        /// </summary>
        public static string PhishingResistant
        {
            get { return "http://schemas.openid.net/pape/policies/2007/06/phishing-resistant"; }
        }

        /// <summary>
        /// Returns the URI for Multi-Factor Authentication
        /// </summary>
        public static string MultiFactor
        {
            get { return "http://schemas.openid.net/pape/policies/2007/06/multi-factor"; }
        }

        /// <summary>
        /// Returns the URI for Physical Multi-Factor Authentication
        /// </summary>
        public static string PhysicalMultiFactor
        {
            get { return "http://schemas.openid.net/pape/policies/2007/06/multi-factor-physical"; }
        }
    }

}
